#include<iostream>
using namespace std;
int main()
{
    long long int n;
    long long int k=0;
    cin>>n;
    if(n<=9)
        k=n;
    else if(n<=99)
        k=9+(n-9)*2;
    else if(n<=999)
        k=9+90*2+(n-99)*3;
    else if(n<=9999)
        k=9+90*2+900*3+(n-999)*4;
    else if(n<=99999)
        k=9+90*2+900*3+9000*4+(n-9999)*5;
    else if(n<=999999)
        k=9+90*2+900*3+9000*4+90000*5+(n-99999)*6;
    else if(n<=9999999)
        k=9+90*2+900*3+9000*4+90000*5+900000*6+(n-999999)*7;
    else if(n<=99999999)
        k=9+90*2+900*3+9000*4+90000*5+900000*6+9000000*7+(n-9999999)*8;
    else if(n<=999999999)
        k=9+90*2+900*3+9000*4+90000*5+900000*6+9000000*7+90000000*8+(n-99999999)*9;
    else
        k=8888888899;
    cout<<k<<endl;
    return 0;
}
